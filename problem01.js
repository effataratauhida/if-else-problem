

// ১.যদি সংখ্যা বিজোড় হয় কিনা তা পরীক্ষা করা
// একটি সংখ্যা ইনপুট নাও। যদি সংখ্যা বিজোড় হয়,তাহলে কনসোলে "Odd" প্রিন্ট করবে,নয়তো "Even" প্রিন্ট করবে।

var number = 22;

if (number % 2 === 0) { 
    console.log('This is a Even number');
}  else {
    console.log('This is a Odd number');
}




