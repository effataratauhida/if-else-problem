
// ৯. পজিটিভ, নেগেটিভ বা জিরো চেক
// সংখ্যা পজিটিভ হলে "Positive" প্রিন্ট,  নেগেটিভ হলে "Negative" প্রিন্ট। শূন্য হলে "Zero" প্রিন্ট করো।

var number = 20;

if (number > 0) {
    console.log("Positive");
}
else if (number < 0) {
    console.log("Negative");
} 

else {
    console.log("Zero");
}